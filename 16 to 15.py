#if_else
var=int(input("enter a number"))
if var>0:
    print(f"{var} is positive number")
else:
    print(f"{var} is  not positive number")
if var%2==0:
    print(f"{var} is even")
else:
    print(f"{var} is odd")
    
if var%5==0:
     print(f"{var} is multiple of 5")
else:
    print(f"{var} is nor a multiple of 5")
    
